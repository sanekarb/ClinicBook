// API to cancel appointment for doctor panel
const appointmentCancel = async (req, res) => {
    try {

        const { docId, appointmentId } = req.body

        const appointmentData = await appointmentModel.findById(appointmentId)
        
        if (appointmentData && appointmentData.docId === docId) {
            await appointmentModel.findByIdAndUpdate(appointmentId, { cancelled: true })

            // =======================================================
            // 🚀 CUSTOM FEATURE: SEND EMAIL (DOCTOR CANCELLED)
            // =======================================================
            const userData = await userModel.findById(appointmentData.userId); 
            const doctorData = await doctorModel.findById(docId); 

            const transporter = nodemailer.createTransport({
                service: 'gmail',
                auth: {
                    user: process.env.EMAIL_USER,
                    pass: process.env.EMAIL_PASS,
                },
            });

            const mailOptions = {
                from: process.env.EMAIL_USER,
                to: userData.email,
                subject: 'Appointment Cancellation Notice - ClinicBook',
                text: `Hello ${userData.name},\n\nWe regret to inform you that your appointment with Dr. ${doctorData.name} scheduled on ${appointmentData.slotDate} at ${appointmentData.slotTime} has been cancelled by the Doctor due to unforeseen circumstances.\n\nPlease book a new slot from your dashboard at your convenience.\n\nRegards,\nClinicBook Team`
            };

            try {
                await transporter.sendMail(mailOptions);
            } catch (emailError) {
                // Error handled silently to avoid API crash
            }
            // =======================================================

            return res.json({ success: true, message: 'Appointment Cancelled & Notification Sent' })
        }

        res.json({ success: false, message: 'Cancellation Failed' }) // Fixed the wrong message bug here

    } catch (error) {
        console.log(error)
        res.json({ success: false, message: error.message })
    }
}















// API to mark appointment completed for doctor panel
const appointmentComplete = async (req, res) => {
    try {

        const { docId, appointmentId } = req.body

        const appointmentData = await appointmentModel.findById(appointmentId)
        if (appointmentData && appointmentData.docId === docId) {
            await appointmentModel.findByIdAndUpdate(appointmentId, { isCompleted: true })
            return res.json({ success: true, message: 'Appointment Completed' })
        }

        res.json({ success: false, message: 'Appointment Not Found' })

    } catch (error) {
        console.log(error)
        res.json({ success: false, message: error.message })
    }

}

